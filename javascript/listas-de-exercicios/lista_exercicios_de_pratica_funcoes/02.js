// Exercício 2: Soma de Dois Números (Declaração Tradicional)
// Crie uma função chamada somar que recebe dois parâmetros, a e b, e retorna a soma deles.

function somar(a, b) {
    return a + b;
}

// Testando a função
const resultado1 = somar(5, 3);
console.log(`A soma de 5 e 3 é: ${resultado1}`);

const resultado2 = somar(10, 20);
console.log(`A soma de 10 e 20 é: ${resultado2}`);

const resultado3 = somar(-5, 15);
console.log(`A soma de -5 e 15 é: ${resultado3}`);

