// EXERCÍCIO 6: ÁREA DE UM CÍRCULO
// Calcule a área de um círculo dado o seu raio. Use a fórmula Área = π * raio²

console.log('=== EXERCÍCIO 6: ÁREA DE UM CÍRCULO ===');

const raio = 5;
const area = Math.PI * Math.pow(raio, 2);

console.log(`Raio: ${raio}`);
console.log(`Área do círculo: ${area.toFixed(2)}`);
