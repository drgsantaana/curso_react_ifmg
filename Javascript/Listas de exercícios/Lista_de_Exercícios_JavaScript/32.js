// Dado a idade de uma pessoa, determine se ela pode tirar habilitação (>= 18 anos)

console.log('=== EXERCÍCIO 32: CATEGORIA DE HABILITAÇÃO ===');

const idade = 20;

if (idade >= 18) {
    console.log(`Idade: ${idade} - PODE tirar habilitação`);
} else {
    console.log(`Idade: ${idade} - NÃO PODE tirar habilitação ainda`);
}
