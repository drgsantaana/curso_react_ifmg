// EXERCÍCIO 25: APROVADO OU REPROVADO
// Com base em uma nota (0-100), determine se um aluno foi aprovado (>= 60) ou reprovado

console.log('=== EXERCÍCIO 25: APROVADO OU REPROVADO ===');

const nota = 75;

if (nota >= 60) {
    console.log(`Nota: ${nota} - APROVADO`);
} else {
    console.log(`Nota: ${nota} - REPROVADO`);
}
