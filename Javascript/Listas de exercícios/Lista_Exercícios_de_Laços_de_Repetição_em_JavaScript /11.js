// Exercício 11: Verificação de Condição (Simulada)
// Simule uma condição que se torna verdadeira após algumas iterações. Por exemplo, um contador que começa em 0 e o laço continua até que o contador atinja 3. Imprima o valor do contador em cada iteração.

let contador = 0;

do {
    console.log('Valor do contador:', contador);
    contador++;
} while (contador < 3);
